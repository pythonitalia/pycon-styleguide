const staggerItems = stagger(0.2, { startDelay: 0 });

export const HeroIllustrationBologna = ({
  cycle,
}: { cycle: "day" | "night" }) => {
  const [isNight, setIsNight] = useState(cycle === "night");

  const [scope, animate] = useAnimate();
  const snakeScaleX = useMotionValue(1);
  const snakeX = useMotionValue(400);
  const snakeY = useMotionValue(600);
  const snakeTailX = useMotionValue(1300);
  const snakeTailY = useMotionValue(600);
  const snakeOpacity = useMotionValue(0);
  const cocktailX = useMotionValue(50);
  const cocktailOpacity = useMotionValue(0);
  const nightTailOpacity = useMotionValue(0);
  const backgroundColor = useMotionValue(isNight ? "#151C28" : "#6A80EF");

  const cocktailXEnd = -180;

  const animateClouds = (enter: boolean, delay = 0.2) => {
    animate(
      ".cloud",
      { right: enter ? ["-200px", "100px"] : ["100px", "-200px"] },
      { duration: 0.5, delay },
    );
    animate(
      ".cloud-2",
      { right: enter ? ["-200px", "150px"] : ["150px", "-200px"] },
      { duration: 0.5, delay: delay + 0.1 },
    );
  };

  const dayAnimation = () => {
    animate(
      ".landmark",
      { bottom: ["-100%", "0%"] },
      {
        duration: 0.5,
        delay: staggerItems,
      },
    ).then(async () => {
      snakeOpacity.set(1);

      await animate(snakeY, -40, { duration: 0.5, ease: "linear" });
      await animate(snakeTailY, 120, { duration: 0.5, ease: "linear" });
      animate(snakeTailY, [140, 160, 140, 160, 140, 160], {
        duration: 2.3,
        delay: 0,
        ease: "easeInOut",
      });
      animate(snakeTailX, 680, {
        duration: 2.4,
        delay: 0,
        ease: "easeInOut",
      });
      animate(snakeY, [-40, -20, -40, -20, -40, -20, -60], {
        duration: 2.3,
        delay: 0,
        ease: "easeInOut",
      });
      animate(snakeX, -360, { duration: 2.4, delay: 0, ease: "easeInOut" });
    });

    animateClouds(true);
  };

  const nightAnimation = async () => {
    nightTailOpacity.set(1);
    cocktailOpacity.set(1);
    snakeOpacity.set(1);
    snakeX.set(-360);
    snakeScaleX.set(-1);

    animate(snakeY, -60, { duration: 0.5, delay: 1.4, ease: "linear" });
    await animate(
      ".landmark",
      { bottom: ["-100%", "0%"] },
      {
        duration: 0.5,
        delay: staggerItems,
      },
    );

    await animate(cocktailX, cocktailXEnd, { duration: 0.5, delay: 0.6 });

    animate(
      ".stars",
      {
        opacity: [0, 1],
      },
      { duration: 0.3, delay: 0.1 },
    );
  };

  useEffect(() => {
    if (isNight) {
      nightAnimation();
    } else {
      dayAnimation();
    }
  }, []);

  const toggle = async () => {
    setIsNight(!isNight);

    animate(backgroundColor, isNight ? "#6A80EF" : "#151C28", {
      duration: 0.5,
    });

    if (isNight) {
      animate(".stars", { opacity: 0 }, { duration: 0.5 });
      animateClouds(true, 0);
    } else {
      animate(".stars", { opacity: 1 }, { duration: 0.5 });
      animateClouds(false, 0);
    }

    await Promise.all([
      animate(snakeY, 1000, { duration: 0.5, ease: "linear" }),
      animate(snakeTailY, 1000, { duration: 0.5, ease: "linear" }),
      animate(
        ".church, .two-towers",
        { y: 1000, x: "-50%" },
        { duration: 0.5, ease: "linear" },
      ),
    ]);

    nightTailOpacity.set(isNight ? 0 : 1);
    cocktailOpacity.set(isNight ? 0 : 1);
    snakeScaleX.set(isNight ? 1 : -1);
    snakeTailX.set(680);

    await Promise.all([
      animate(snakeY, -60, { duration: 0.5, ease: "linear" }),
      animate(
        ".church, .two-towers",
        { y: 0, x: "-50%" },
        { duration: 0.5, ease: "linear" },
      ),
      isNight && animate(snakeTailY, 100, { duration: 0.5, ease: "linear" }),
      !isNight &&
        animate(cocktailX, cocktailXEnd, { duration: 0.5, delay: 0.6 }),
    ]);
  };

  if (typeof window !== "undefined") {
    (window as any).toggle = toggle;
  }

  return (
    <motion.div
      className={clsx("w-full h-full overflow-hidden")}
      style={{ backgroundColor }}
    >
      <div className="relative w-full h-full" ref={scope}>
        <div className="landmark absolute -bottom-full min-w-full flex justify-center z-10 left-1/2 -translate-x-1/2">
          <Hills1 isNight={isNight} />
        </div>

        <div className="landmark absolute -bottom-full flex justify-center min-w-full z-[5] left-1/2 -translate-x-1/2">
          <AnimatePresence>
            <motion.div
              className="relative left-[-550px] top-[-550px]"
              initial={{ top: 0 }}
              animate={{ top: -550 }}
              exit={{ top: 0 }}
              key={isNight ? "moon" : "sun"}
              transition={{ duration: 0.5 }}
            >
              <div className="absolute">{isNight ? <Moon /> : <Sun />}</div>
            </motion.div>
          </AnimatePresence>
        </div>

        <div className="landmark absolute -bottom-full min-w-full flex justify-center z-20 left-1/2 -translate-x-1/2">
          <Hills2 isNight={isNight} />
        </div>
        <div className="landmark absolute -bottom-full min-w-full flex justify-center z-30 left-1/2 -translate-x-1/2 pointer-events-none">
          <Hills3 isNight={isNight} />
        </div>
        <div className="landmark absolute -bottom-full min-w-full flex justify-center z-30 left-1/2 -translate-x-1/2 pointer-events-none">
          <Hills4 isNight={isNight} />
        </div>

        <motion.div
          className="landmark church absolute -bottom-full min-w-full flex justify-center z-20 left-1/2 -translate-x-1/2"
          animate={{
            x: "-50%",
          }}
        >
          <motion.div
            className="absolute z-10"
            style={{
              translateX: cocktailX,
              translateY: 350,
              opacity: cocktailOpacity,
            }}
          >
            <Cocktail />
          </motion.div>

          <div className="relative z-20">
            <Church onClick={toggle} />
          </div>

          <motion.div
            className="night-tail absolute bottom-[275px] left-[180px] min-w-full flex justify-center z-30 pointer-events-none"
            style={{
              opacity: nightTailOpacity,
            }}
          >
            <div className="relative">
              <NightTail />
            </div>
          </motion.div>
        </motion.div>

        <motion.div
          className="landmark two-towers absolute -bottom-full min-w-full flex justify-center z-30 left-1/2 -translate-x-1/2 pointer-events-none"
          animate={{ x: "-50%", translateX: 550 }}
        >
          <div>
            <TwoTowers />
          </div>
        </motion.div>

        <div className="cloud absolute bottom-[700px] -right-full z-10">
          <Cloud />
        </div>

        <div className="cloud-2 absolute bottom-[580px] -right-full z-10">
          <Cloud />
        </div>

        <div className="stars absolute opacity-0 top-0 min-w-full flex justify-center z-0 left-1/2 -translate-x-1/2">
          <Stars />
        </div>

        <motion.div
          className="snake absolute bottom-[200px] left-[100px] min-w-full flex justify-center z-10"
          style={{
            x: snakeX,
            y: snakeY,
            opacity: snakeOpacity,
            scaleX: snakeScaleX,
          }}
        >
          <Snake />
        </motion.div>

        <motion.div
          className="snake-tail absolute bottom-[200px] left-[100px] min-w-full flex justify-center z-10"
          style={{ x: snakeTailX, y: snakeTailY, opacity: snakeOpacity }}
        >
          <SnakeTail />
        </motion.div>
      </div>
    </motion.div>
  );
};
